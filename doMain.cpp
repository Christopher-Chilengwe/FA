void doMain()
{
	vector<Account*>* pv_pca;
	pv_pca = (new vector<Account*>);
	
	mainMenu(pv_pca);

	for(uint_t i = 0; i < pv_pca->size(); ++i) 
	{ 
		delete pv_pca->at(i); 
	}
	delete pv_pca;
}
