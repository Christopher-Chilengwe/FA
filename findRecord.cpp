lint_t findRecord(vector<Account*>*& pv_pca, const field_t ssn)
{
	for(uint_t i = 0; i < pv_pca->size(); ++i)
	{
		if(pv_pca->at(i)->customerSocial() == ssn) 
		{ 
			return (lint_t(i));
		}
	}

	return(RECORD_NOT_FOUND);
}
