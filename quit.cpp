char quit()
{
	char isQuit = (FALSE);

	do
	{
		cout << '\n' << IS_QUIT;
		cin >> isQuit;

		isQuit = toupper(isQuit);

		if(isQuit == TRUE || isQuit == FALSE)
		{
			break;
		}
	}while(isQuit != TRUE && isQuit != FALSE);

	return(isQuit);
}
